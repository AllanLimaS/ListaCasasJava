    private void SalvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarButtonActionPerformed
        // TODO add your handling code here:
        
        boolean podeSalvar = true;
        String erro = "";
        
        Casa casa = new Casa();
        
        try{
            casa.setNumero(Integer.parseInt(numeroText.getText()));
        }catch (NumberFormatException e){
            casa.setNumero(0);
            podeSalvar = false;
            erro = erro + "NUMERO ERRADO \n";
        }
        
        if(corText.getText().matches("-?\\d+")){
            casa.setCor("branco");
            podeSalvar = false;
            erro = erro + "COR ERRADA \n";
        }else{
            casa.setCor(corText.getText());
        }
        
        try{
            casa.setNumAndares(Integer.parseInt(andaresText.getText()));
        }catch (NumberFormatException e){
            casa.setNumAndares(0);
            podeSalvar = false;
            erro = erro + "ANDARES ERRADO \n";
        }
        
        try{
            casa.setArea(Float.parseFloat(areaText.getText()));
        }catch (NumberFormatException e){
            casa.setArea(0);
            podeSalvar = false;
            erro = erro + "AREA ERRADA \n";
        }
        
        if(materialText.getText().matches("-?\\d+")){
            casa.setMaterial("madeira");
            podeSalvar = false;
            erro = erro + "MATERIAL ERRADO \n";
            
        }else{
            casa.setMaterial(materialText.getText());
        }
        
        try{
            casa.setJanelas(Integer.parseInt(janelasText.getText()));
        }catch(NumberFormatException e){
            casa.setJanelas(0);
            podeSalvar = false;
            erro = erro + "JANELAS ERRADA \n";
            
        }
        
        if(podeSalvar == true ){
            try {
                casa.SalvaCasa();
                JOptionPane.showMessageDialog(this,"salvo com sucesso");
                numeroText.setText("");
                corText.setText("");
                andaresText.setText("");
                areaText.setText("");
                materialText.setText("");
                janelasText.setText("");
            } catch (FileNotFoundException ex) {
                Logger.getLogger(InserirFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(InserirFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(this,erro);
        }
        
    }//GEN-LAST:event_SalvarButtonActionPerformed

